<strong>What Is Curves?</strong><br>
Curves is the largest fitness franchise in the world with over 8,000 locations across the globe. It's the first fitness and weight loss facility dedicated to providing affordable, one-stop, exercise and nutritional guidance to women working towards achieving their fitness goals.<br>
<br>

Curves founder Gary Heavin and his wife Diane opened the first Curves in Harlingen, TX in 1992. This new 30 Minute fitness program was immediately successful. Together Gary and Diane franchised the concept in 1995. Curves climbed to 1,000 locations in 36 months. This phenomenal growth is attributed almost entirely to word-of-mouth from Curves' members. Simply put, Curves works and Curves' members love it!<br>
<br>

<strong>Curves Member Audience Composition</strong><br>
The Curves' member is an educated woman primarily 30+ years old with a mean HH income of $80M. She is married with children and 64% are employed outside the home. She is heavily involved in fitness & nutrition and cares about her health and appearance.<br>
