<h1>College Bookstore Bag Sampling Program</h2>

<strong>Program Description</strong><br>

<img src="images/bookstore1.jpg" alt="" class="right">

Place small product samples and promotional offers/coupons in up to 8 million bags annually at college bookstores nationwide.  The bags are used by students for their text book purchases.<br>
<br>

The College Bookstore Bag Sampling Program provides a unique and exclusive opportunity for brands to reach the elusive college student. Products or offers are inserted at the bag manufacturing plant, eliminating the need for retail execution. Regional, targeted programs are available with participating retailers using custom, branded shopping bags.<br>
<br>

<strong>Key Elements</strong><br>
<ul>
  <li>Samples due for Fall semester (August book rush), May</li>
  <li>Samples due for Spring semester (January book rush), October</li>
  <li>Brands may select customized distribution</li>
</ul>
