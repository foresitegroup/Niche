<h1>NSI Market Research</h2>

NSI's co-op sampling programs include independent market research conducted by WB&A. The objective of the market research is to measure the impact the product samples and offers/coupons had on purchasing behavior. Brands supply their own questions which typically measure changes in brand awareness, usage and purchase.<br>
<br>
 
NSI utilizes a test versus control cell design 2-3 months after distribution. The test group is composed of target audience sample recipients. The control group is comprised of target audience members who did not receive a sample. WB&A & NSI provide a research report on individual brand performance including ROI and a proprietary payback analysis.

